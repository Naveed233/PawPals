export type IconName = keyof typeof STROKE_PATHS | keyof typeof FILL_PATHS;

/** Build an expo-image source for an icon in the given color. */
export function iconSource(name: IconName, color: string, strokeWidth = 2) {
  const fill = FILL_PATHS[name as string];
  const body = fill
    ? fill.replaceAll('COLOR', color)
    : `<g fill="none" stroke="${color}" stroke-width="${strokeWidth}" stroke-linecap="round" stroke-linejoin="round">${STROKE_PATHS[name as string]}</g>`;
  return {
    uri:
      'data:image/svg+xml;utf8,' +
      encodeURIComponent(`<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24">${body}</svg>`),
  };
}

export function Icon({
  name,
  color,
  size = 22,
  strokeWidth = 2,
  style,
}: {
  name: IconName;
  color: string;
  size?: number;
  strokeWidth?: number;
  style?: StyleProp<ViewStyle>;
}) {
  return (
    <Image
      source={iconSource(name, color, strokeWidth)}
      style={[{ width: size, height: size }, style as object]}
      contentFit="contain"
    />
  );
}
